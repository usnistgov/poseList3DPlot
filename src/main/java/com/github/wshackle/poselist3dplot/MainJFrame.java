    public void saveCsvFile(final File f) throws IOException {
        try (PrintWriter pw = new PrintWriter(new FileWriter(f))) {
            pw.println(TrackPoint.HEADER);
            this.view3DPlotJPanel1
                    .getTracksList()
                    .stream()
                    .flatMap(x -> x.stream())
                    .map(x -> x.getData())
                    .flatMap(x -> x.stream())
                    .map(x -> x.toString())
                    .forEach(x -> pw.println(x));
        }
    }

